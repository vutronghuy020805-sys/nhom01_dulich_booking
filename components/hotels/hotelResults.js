export const getHotelsBySlug = (locationSlug) =>
  (hotelsByLocation[locationSlug] || []).map((h) =>
    decorateHotel(h, locationSlug)
  );

export const getHotelByDetailSlug = (locationSlug, hotelSlug) => {
  const list = hotelsByLocation[locationSlug] || [];
  const match = list.find(
    (h) => (h.slug || slugify(h.name)) === hotelSlug
  );
  return match ? decorateHotel(match, locationSlug) : null;
};

export const getRatePlanById = (
  locationSlug,
  hotelSlug,
  roomSectionId,
  ratePlanId
) => {
  const hotel = getHotelByDetailSlug(locationSlug, hotelSlug);
  if (!hotel) return null;
  const roomSection = (hotel.roomSections || []).find(
    (s) => s.id === roomSectionId
  );
  if (!roomSection) return null;
  const ratePlan = (roomSection.ratePlans || []).find(
    (p) => p.id === ratePlanId
  );
  if (!ratePlan) return null;
  return { hotel, roomSection, ratePlan };
};

